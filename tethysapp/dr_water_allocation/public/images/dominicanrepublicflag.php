<div class="factstitle">Dominican Republic Flag - Information</div>
<div class="factsbox">The national flag of Dominican Republic features a centered white cross that extends to the edges and divides the flag into four rectangles; the top ones are blue (hoist side) and red, and the bottom ones are red (hoist side) and blue. The national coat of arms, featuring a shield with the flag design and supported by a bay laurel branch (left) and a palm frond (right), is at the center of the cross. Above the shield, a blue ribbon displays the national motto Dios, Patria, Libertad (English: God, Fatherland, Liberty). Below the shield, the words República Dominicana appear on a red ribbon (this red ribbon is depicted in more recent versions as having its tips pointing upward). In the center of the shield, flanked by three spears (two of them holding Dominican banners) on each side, is a Bible with a small cross above it and said to be opened to the Gospel of John, chapter 8, verse 32, which reads Y la verdad os hará libres (And the truth shall make you free)
